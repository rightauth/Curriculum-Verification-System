function getGradeFromHTMLText(htmlText){
	var subjectCodePattern = ">=*\n*[0-9]=*\n*[0-9]=*\n*[0-9]=*\n*[0-9]=*\n*[0-9]=*\n*[0-9]=*\n*[0-9]=*\n*[0-9]=*\n*<";
	// var txt = htmlText.slice( htmlText.search(subjectCodePattern) );
    var txtList = htmlText.match(/>[A-Za-z0-9,= \+\n\-]+<=*\n*\/=*\n*f=*\n*o=*\n*n=*\n*t=*\n*>/g)
    var re = new RegExp(subjectCodePattern);
    var operation = -1;
    var resultGradeList = [];
    var objSubject = {};
    var i = 0;
    for (var t of txtList){
        if (t.includes("\t"))
            continue;
        if ( re.test(t) ){
            operation = 0;
        }
        if ( operation == 0 ) {
            objSubject.subject_code = t;
            operation++;
        }
        else if ( operation == 1 ) {
            objSubject.subject_name_en = t;
            operation++;
        }
        else if ( operation == 2 ) {
            objSubject.grade = t;
            operation++;
        }
        else if ( operation == 3 ) {
            objSubject.credit = parseInt(t);
            operation++;
            resultGradeList.push( objSubject )
            objSubject = {};
        }
    }
	return resultGradeList;
}

